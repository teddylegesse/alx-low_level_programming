/**
 * add - addition of two number
 * @a: integer argument
 * @b: integer argument
 * Return: a+b
 */
int add(int a, int b)
{
      return (a + b);
}
/**
 * sub - substruction of two numbers
 * @a: integer argument
 * @b: integer argument
 * Return: a-b
 */
int sub(int a, int b)
{
      return (a - b);
}
/**
 * mul - multiplication of two numbers
 * @a: integer argument
 * @b: integer argument
 * Return: a*b
 */
int mul(int a, int b)
{
      return (a * b);
}
/**
 * div - division of two numbers
 * @a: integer argument
 * @b: integer argument
 * Return: a/b
 */
int div(int a, int b)
{
      return (a / b);
}
/**
 * mod - mod of two number
 * @a: integer argument
 * @b: integer argument
 * Return: a%b
 */
int mod(int a, int b)
{
      return (a % b);
}
